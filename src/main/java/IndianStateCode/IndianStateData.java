package IndianStateCode;

public class IndianStateData {
    int SrNo;
    String stateName;
    int tin;
    String stateCode;

    public IndianStateData(int srNo, String stateName, int tin, String stateCode) {
        SrNo = srNo;
        this.stateName = stateName;
        this.tin = tin;
        this.stateCode = stateCode;
    }
}
